function [ code_dec ] = CodingReal2Dec( population,lb,ub,len_code )
%将个体（染色体）编码为十进制编码
%--------------------------------------------------------
%	输入变量
%	population：待编码种群
%   lb：设计变量的下限向量
%	ub：设计变量的上限向量
%	len_code：编码长度
%--------------------------------------------------------
%	输出变量
%	code_dec：种群的十进制编码
%--------------------------------------------------------
code_dec = (2^len_code-1)*(population-lb)./(ub-lb);
end