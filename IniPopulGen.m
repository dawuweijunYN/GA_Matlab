function [ popul_ini ] = IniPopulGen( num_ini,lb,ub )
%生成初始种群
%--------------------------------------------------------
%	输入变量
%	num_ini：种群的数量
%   lb：设计变量的下限向量
%	ub：设计变量的上限向量
%--------------------------------------------------------
%	输出变量
%	popul_ini：初始种群
%--------------------------------------------------------
num_var = length(lb);
rand01 = rand(num_var,num_ini);
popul_ini = lb+rand01.*(ub-lb);
end