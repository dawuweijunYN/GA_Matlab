function [ population ] = DecodingDec2Real( code_dec,lb,ub,len_code )
%十进制编码解码为个体（染色体）
%--------------------------------------------------------
%	输入变量
%	code_dec：种群的十进制编码
%   lb：设计变量的下限向量
%	ub：设计变量的上限向量
%	len_code：编码长度
%--------------------------------------------------------
%	输出变量
%	population：解码后的种群
%--------------------------------------------------------
	population = code_dec.*(ub-lb)/(2^len_code-1)+lb;
end